public class PersonalData {
    String id;
    String name;
    String address;

    public PersonalData(){
        this.id = null;
        this.name = null;
        this.address = null;
    }

    public PersonalData(String id, String name, String address){
        this.id = id;
        this.name = name;
        this.address = address;
    }

}
